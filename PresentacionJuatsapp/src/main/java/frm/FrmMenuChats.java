    private void cargarMensajes(ChatDTO chat) {
        if (chat != null) {
            this.txtAreaMensajes.setText("");
            this.txtMensaje.setEditable(true);
            control.setChatAbierto(chat);
            List<MensajeDTO> mensajesChat;
            try {
                mensajesChat = funcionalidadesChat.consultarMensajes(control.getChatAbierto());
                if (!mensajesChat.isEmpty()) {
                    for (MensajeDTO mensaje : mensajesChat) {
                        String fecha = this.cambiarFormato(mensaje.getFechaHoraEnviado());
                        String nombre;
                        if (mensaje.getEmisor().equals(control.getUsuarioActivo())) {
                            nombre = "Tu";
                        } else {
                            nombre = mensaje.getEmisor().getNombre();
                        }
                        String estructura = String.format("%s:  %s  \n%s\n", nombre, mensaje.getTexto(), fecha);
                        this.txtAreaMensajes.append(estructura);
                    }
                }
            } catch (NegocioException ex) {
                Logger.getLogger(FrmMenuChats.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void enviarMensaje() {
        if (control.getChatAbierto() != null) {
            MensajeDTO mensaje = new MensajeDTO(control.getUsuarioActivo(), this.txtMensaje.getText());
            try {
                funcionalidadesChat.guardarMensaje(control.getChatAbierto(), mensaje);
                this.txtMensaje.setText("");
                this.cargarMensajes(control.getChatAbierto());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al enviar el mensaje.");
            }
        }

    }

    private String cambiarFormato(Date date) {
        String strDateFormat = "dd-MM-yyyy HH:mm";
        SimpleDateFormat objSDF = new SimpleDateFormat(strDateFormat);
        String fecha = objSDF.format(date);
        return fecha;
    }

    private void llenarListaChats() {
        try {
            this.listaChats = funcionalidadesChat.obtener(control.getUsuarioActivo());
        } catch (NegocioException ex) {
            Logger.getLogger(FrmMenuChats.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarChats() {
        for (ChatDTO chat : listaChats) {
            modeloChats.addElement(chat);
        }
    }

